package com.domain;

public class GoodsList {
	/**
	 * name=商品名 price=商品价格 num=商品数量
	 */
	String name;
	float price;
	int num;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public float getPrice() {
		return price;
	}

	public void setPrice(float price) {
		this.price = price;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}
}
